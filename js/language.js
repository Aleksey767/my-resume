const allLang = ['RU', 'EN']
const select = document.getElementsByClassName('select');
select[1].addEventListener('click', changeURLEng);
select[0].addEventListener('click', changeURLRu);


function changeURLRu() {

    let lang = select[0].innerHTML
    location.href = window.location.pathname + '#' + lang;
    location.reload();


}

function changeURLEng() {
    let lang = select[1].innerHTML
    location.href = window.location.pathname + '#' + lang;
    location.reload();
}

function changeLanguage() {
    if(location.href.includes('RU')){
        const flag = document.querySelector('.flag');
        const dropbtn = document.querySelector('.dropbtn');
        flag.style.display=('block');
        dropbtn.style.display=('none');
    }
    let hash = window.location.hash;
    hash = hash.substr(1);
    if (!allLang.includes(hash)) {
        location.href = window.location.pathname + '#EN';
        hash = select[1].innerHTML
    }

    document.querySelector('title').innerHTML = langArr['title'][hash];
    for (let key in langArr) {
        let elem = document.querySelector('.lng-' + key);
        if (elem) {
            elem.innerHTML = langArr[key][hash];
        }
    }
}
changeLanguage();